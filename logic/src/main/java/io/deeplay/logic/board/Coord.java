package io.deeplay.logic.board;

public class Coord {
    private int row;
    private int column;

    public int getRow() {
        return row;
    }

    public void setRow(final int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(final int column) {
        this.column = column;
    }
}

